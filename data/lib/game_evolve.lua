function checkPokemonEvolve(cid)
	local name = getCreatureName(cid)
	local getEvolution = evolveWithLevel[name]
	local levelPoke = getItemAttribute(getPlayerSlotItem(getCreatureMaster(cid), 8).uid, "pokeLevel")
	if getEvolution and levelPoke >= getEvolution.levelToEvolve then
		return doEvolvePokemon(getCreatureMaster(cid), {uid = cid}, getEvolution.nextEvolve)
	end
	return true
end